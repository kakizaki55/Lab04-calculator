export function add(nm1, nm2) {
    return nm1 + nm2;
    
}

export function sub(num1, num2){
    return num1 - num2; 

}

export function mul(num1, num2){
    return num1 * num2; 
}

export function div(num1, num2){
    return num1 / num2 ; 
}

export function mod(num1, num2){
    return num1 % num2; 
}
export function int(num1, num2){
    return Math.round(num1 / num2);
}
